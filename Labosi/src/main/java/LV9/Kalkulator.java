    private void btnDecimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecimalActionPerformed
        if(txtIspis.getText().isEmpty() || txtIspis.getText() == null){
            JFrame f = new JFrame();
            JOptionPane.showMessageDialog(f, "Unesite broj");
            return;
        }
        if(txtIspis.getText().endsWith("+") || txtIspis.getText().endsWith("-") || txtIspis.getText().endsWith(".") || txtIspis.getText().endsWith("%") || txtIspis.getText().endsWith("*")){
           txtIspis.setText((txtIspis.getText().substring(0,txtIspis.getText().length() - 1) ));
            txtIspis.setText(txtIspis.getText() + ".");
        }else{
            txtIspis.setText(txtIspis.getText() + ".");
        }
    }//GEN-LAST:event_btnDecimalActionPerformed

