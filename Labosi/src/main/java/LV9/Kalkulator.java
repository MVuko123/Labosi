    private void btnJednakkoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJednakkoActionPerformed
       if (txtIspis.getText().isEmpty() || txtIspis.getText() == null) {
            JOptionPane.showMessageDialog(null, "Unesite nesto");
            return;
        }
        ScriptEngineManager mgr = new ScriptEngineManager();
        ScriptEngine engine = mgr.getEngineByName("JavaScript");
        String izracun = txtIspis.getText();
        try {
            txtIspis.setText(engine.eval(izracun).toString());
        } catch (ScriptException ex) {
            JOptionPane.showMessageDialog(null, "Krivi unos, pokusajte ponovno");
        }
    }//GEN-LAST:event_btnJednakkoActionPerformed

