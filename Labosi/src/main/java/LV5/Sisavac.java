public class Sisavac {
    private int dob; 
    private double tezina; 
    
    public Sisavac(){
        this.dob = 0;
        this.tezina = 0;
    }

    public int getDob() {
        return dob;
    }

    public void setDob(int dob) {
        this.dob = dob;
    }

    public double getTezina() {
        return tezina;
    }

    public void setTezina(double tezina) {
        this.tezina = tezina;
    }
    
}
